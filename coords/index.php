<script>
	$(document).ready(function(e) {
		$('img[usemap]').rwdImageMaps();

		 //area click
        $(".area-rect").on("click", function(e) {
            e.preventDefault();
            let className = $(this).attr('class');
            let idName = $(this).attr('id');
            let idNumber = parseInt(idName.replace(className + '-', ''));
            //$("#popup_" + idNumber).show();
			$("#popup_" + idNumber).css("visibility", "visible");
        });
		
		// close
        $(".btn-close").on("click", function(e) {
            e.preventDefault();
            let className = $(this).attr('class');
            let idName = $(this).attr('id');
            let idNumber = parseInt(idName.replace(className + '-', ''));
            $("#popup_" + idNumber).css("visibility", "hidden");
        });
		
		 //next, prev
        $(".btn-prev, .btn-next").on("click", function(e) {
            e.preventDefault();
            let className = $(this).attr('class');
            let idName = $(this).attr('id');
            let idNumber = parseInt(idName.replace(className + '-', ''));
            var targetId = -1;
            if (className == "btn-prev") {
                if (idNumber > 1) {
                    targetId = idNumber - 1;
                } else {
                    targetId = 9;
                }
            } else {
                if (idNumber < 9) {
                    targetId = idNumber + 1;
                } else {
                    targetId = 1;
                }
            }
            if (targetId > -1) {
                $(".div-popup").css("visibility", "hidden");
                $("#popup_" + targetId).css("visibility", "visible");
            }
        })
		
	});
</script>
